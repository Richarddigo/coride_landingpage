// Get nested property from object using dot notation
function getNestedProperty(obj, path) {
    return path.split('.').reduce((current, prop) => current?.[prop], obj);
}

// Translate the page
function translatePage(lang) {
    const elements = document.querySelectorAll('[data-translate]');

    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getNestedProperty(translations[lang], key);

        if (translation) {
            // For input placeholders
            if (element.tagName === 'INPUT') {
                element.placeholder = translation;
            }
            // For regular text content
            else {
                element.textContent = translation;
            }
        }
    });

    // Save language preference
    localStorage.setItem('cojaunyLanguage', lang);

    // Update active language button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });

    // Update html lang attribute
    document.documentElement.lang = lang;
}

// Initialize language
function initLanguage() {
    // Check if language is saved
    let savedLang = localStorage.getItem('cojaunyLanguage');

    // If no saved language, detect browser language
    if (!savedLang) {
        const browserLang = navigator.language.toLowerCase().split('-')[0];
        savedLang = ['en', 'es', 'de', 'fr'].includes(browserLang) ? browserLang : 'en';
    }

    translatePage(savedLang);
}

// Export for use in app.js
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { translations, translatePage, initLanguage };
}
